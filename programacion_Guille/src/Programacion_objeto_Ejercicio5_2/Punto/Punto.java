package Programacion_objeto_Ejercicio5_2.Punto;

public class Punto {

	private int x;
	private int y;
	
	public Punto(int elX, int ely) 
	{
		x = elX;
		y = ely;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
